#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char *)s;
	if (s1)
	{
		while (s[i])
		{
			s[i] = '\0';
			i++;
		}
	}
}
